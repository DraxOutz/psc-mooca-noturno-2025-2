public class Calculadora {

    public int soma(int a, int b){
        int resultado;
        resultado = a + b;
        return resultado;
        
    }


     public int subtracao(int a, int b){
        int resultado;
        resultado = a - b;
        return resultado;
        
    }

    public int multiplicao(int a, int b){
        int resultado;
        resultado = a * b;
        return resultado;
        
    }

    public int divisao(int a, int b){
        int resultado;
        resultado = a / b;
        return resultado;
        
    }



    
}
